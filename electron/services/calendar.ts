const OAUTH_SCOPES = [
  "https://www.googleapis.com/auth/calendar.readonly",
  "https://www.googleapis.com/auth/userinfo.email",
  "openid",
];

function encryptToken(value: string | null): string | null {
  if (!value) return null;
  if (!safeStorage.isEncryptionAvailable()) {
    return Buffer.from(`plain:${value}`, "utf8").toString("base64");
  }
  return safeStorage.encryptString(value).toString("base64");
}

function decryptToken(value: string | null): string | null {
  if (!value) return null;
  const buf = Buffer.from(value, "base64");
  if (buf.toString("utf8").startsWith("plain:")) {
    return buf.toString("utf8").slice("plain:".length);
  }
  try {
    return safeStorage.decryptString(buf);
  } catch {
    return null;
  }
}

