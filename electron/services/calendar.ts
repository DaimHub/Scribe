const MATCH_WINDOW_MS = 4 * 60 * 60 * 1000;

export interface LinkResult {
  linked: boolean;
  event: CalendarEventRow | null;
  confidence: number;
  reason: string | null;
  titleRenamed: boolean;
}

/**
 * Format the auto-generated meeting title as "<event title> · YYYY-MM-DD".
 * ISO date keeps recurring meetings sortable and locale-independent.
 */
function buildAutoTitle(eventTitle: string, startedAtMs: number): string {
  const d = new Date(startedAtMs);
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, "0");
  const day = String(d.getDate()).padStart(2, "0");
  return `${eventTitle} · ${y}-${m}-${day}`;
}

/**
 * Find the best calendar match for a recording and persist the link if the
 * matcher is confident. Optionally pre-fills the recording's title from the
 * event when it's still the auto-generated default.
 */
export function autoLinkMeeting(opts: {
  meetingId: string;
  renameIfAuto: boolean;
}): LinkResult {
  const meeting = getMeeting(opts.meetingId);
  if (!meeting) return emptyResult();

  // Look at events overlapping a wide window centered on the recording.
  const start = meeting.started_at_ms - MATCH_WINDOW_MS;
  const end =
    (meeting.ended_at_ms ?? meeting.started_at_ms + MATCH_WINDOW_MS) +
    MATCH_WINDOW_MS;
  const events = listCalendarEventsOverlapping(start, end);
  const match = findMatchingEvent(
    {
      id: meeting.id,
      title: meeting.title,
      started_at_ms: meeting.started_at_ms,
      ended_at_ms: meeting.ended_at_ms,
    },
    events,
  );
  if (!match) return emptyResult();

  // If the event is already linked to a different meeting, don't steal it.
  if (match.event.meeting_id && match.event.meeting_id !== meeting.id) {
    return emptyResult();
  }

  // Make sure no other event points at this meeting (one-to-one).
  clearLinkForMeeting(meeting.id);
  linkCalendarEventToMeeting(match.event.id, meeting.id);

  let renamed = false;
  if (opts.renameIfAuto && isMeetingTitleAutoGenerated(opts.meetingId)) {
    setTitle(opts.meetingId, buildAutoTitle(match.event.title, meeting.started_at_ms));
    renamed = true;
  }

  return {
    linked: true,
    event: { ...match.event, meeting_id: meeting.id },
    confidence: match.confidence,
    reason: match.reason,
    titleRenamed: renamed,
  };
}

function emptyResult(): LinkResult {
  return {
    linked: false,
    event: null,
    confidence: 0,
    reason: null,
    titleRenamed: false,
  };
}

/**
 * List nearby calendar events ranked by match score for a recording —
 * powers the manual "Link to event…" picker.
 */
export function listLinkCandidates(meetingId: string): MatchCandidate[] {
  const meeting = getMeeting(meetingId);
  if (!meeting) return [];
  const start = meeting.started_at_ms - 24 * 60 * 60 * 1000;
  const end =
    (meeting.ended_at_ms ?? meeting.started_at_ms) + 24 * 60 * 60 * 1000;
  const events = listCalendarEventsOverlapping(start, end);
  return rankCandidates(
    {
      id: meeting.id,
      title: meeting.title,
      started_at_ms: meeting.started_at_ms,
      ended_at_ms: meeting.ended_at_ms,
    },
    events,
  );
}

