function dateToMs(d?: { dateTime?: string; date?: string }): number | null {
  if (!d) return null;
  if (d.dateTime) return Date.parse(d.dateTime);
  if (d.date) return Date.parse(d.date + "T00:00:00");
  return null;
}

export async function syncAccountEvents(opts: {
  accountId: string;
  fromMs: number;
  toMs: number;
}): Promise<{ count: number }> {
  const account = getCalendarAccount(opts.accountId);
  if (!account) throw new Error("Account not found");
  const token = await getValidAccessToken(account);

  const params = new URLSearchParams({
    singleEvents: "true",
    orderBy: "startTime",
    timeMin: new Date(opts.fromMs).toISOString(),
    timeMax: new Date(opts.toMs).toISOString(),
    maxResults: "250",
  });

  const res = await fetch(
    `https://www.googleapis.com/calendar/v3/calendars/primary/events?${params}`,
    { headers: { Authorization: `Bearer ${token}` } },
  );
  if (!res.ok) {
    const text = await res.text();
    throw new Error(`Calendar list failed: ${res.status} ${text}`);
  }
  const json = (await res.json()) as { items?: GoogleCalendarEvent[] };
  let count = 0;
  for (const ev of json.items ?? []) {
    if (ev.status === "cancelled") continue;
    const start = dateToMs(ev.start);
    const end = dateToMs(ev.end);
    if (start == null || end == null) continue;
    upsertCalendarEvent({
      accountId: account.id,
      sourceEventId: ev.id,
      title: ev.summary ?? "(no title)",
      description: ev.description ?? null,
      location: ev.location ?? null,
      startAtMs: start,
      endAtMs: end,
      hangoutLink: ev.hangoutLink ?? ev.htmlLink ?? null,
      attendeesJson: ev.attendees ? JSON.stringify(ev.attendees) : null,
    });
    count++;
  }
  return { count };
}

